import pandas as pd

# 1. Загружаем данные из файла (это заменяет создание словарей вручную)
df = pd.read_csv('pressure_data.csv')

# 2. Выводим таблицу, чтобы убедиться, что всё подтянулось
print("--- Исходные данные из файла ---")
print(df)

# 3. Пункт №3 из списка: применяем .describe()
print("\n--- Инженерная статистика системы ---")
print(df.describe())

# 4. ОЧИСТКА ДАННЫХ
# Вариант А: Заполняем пустоту нулем (чтобы расчеты не ломались)
df_filled = df.fillna(0) 

# Вариант Б: Или просто удаляем строки, где есть NaN
df_cleaned = df.dropna()

print("\n--- Таблица с заполненными пропусками (NaN -> 0) ---")
print(df_filled)

# 5. ДОБАВЛЯЕМ КОЛОНКУ СТАТУСА
# Пишем логику: если давление > 20 - "OK", если меньше - "Check!", lambda в коде — это временная «перемычка». 
df_filled['Status'] = df_filled['Pressure'].apply(lambda x: 'OK' if x > 20 else 'Check!') 

# 6. КРАСИВЫЙ ВЫВОД (Округление до 1 знака)
print("\n--- Итоговый инженерный отчет ---")
# Используем f-строку для заголовка и метод round() для таблицы
print(df_filled.round(1))
